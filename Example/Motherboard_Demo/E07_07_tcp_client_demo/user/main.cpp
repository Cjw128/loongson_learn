// 另外一端的IP地址
#define SERVER_IP "192.168.93.123"
// 端口号
#define PORT 8888

uint32  read_len = 0;
uint8   recv_buff[1024];
uint8   temp_str[] = "seekfree this is TCP demo.\r\n";
uint8   read_str[] = "read data:\r\n";

int main(int, char**) 
{

    // 初始化TCP客户端,需要先打开TCP服务器,这才不会卡主。
    // 初始化TCP客户端,需要先打开TCP服务器,这才不会卡主。
    // 初始化TCP客户端,需要先打开TCP服务器,这才不会卡主。
    if(tcp_client_init(SERVER_IP, PORT) == 0)
    {
        printf("tcp_client ok\r\n");
    }
    else
    {
        printf("tcp_client error\r\n");
        return -1;
    }

    // 先发消息，另外一端的TCP知道本机的IP地址。
    tcp_client_send_data(temp_str, sizeof(temp_str));
    tcp_client_send_data(temp_str, sizeof(temp_str));
    tcp_client_send_data(temp_str, sizeof(temp_str));

    while (true) 
    {
        // 非阻塞式读取TCP数据。
        read_len = tcp_client_read_data(recv_buff, 1024);
        // 回显TCP数据。
        if(read_len > 0)
        {
            tcp_client_send_data(read_str, sizeof(read_str));
            tcp_client_send_data(recv_buff, read_len);
        }
  
        system_delay_ms(1000);
    }

    return 0;
}


